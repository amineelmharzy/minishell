#include "minishell.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	i;
	int	len;

	if (!s || !fd)
		return ;
	i = 0;
	len = ft_strlen(s);
	while (i < len)
	{
		write(fd, &s[i], 1);
		i++;
	}
}
